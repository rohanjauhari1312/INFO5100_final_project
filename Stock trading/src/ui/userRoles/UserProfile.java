    private void btnRemoveOrderItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveOrderItemActionPerformed
        int selectedRowIndex = tblCart.getSelectedRow() ;
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select the order item first.");
            return;
        }
        OrderItem item = (OrderItem) tblCart.getValueAt(selectedRowIndex, 0);
        currentOrder.deleteltem(item);

        populateCartTable();
    }//GEN-LAST:event_btnRemoveOrderItemActionPerformed

    private void btnModifyQuantityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifyQuantityActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblCart.getSelectedRow();
        if (selectedRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please select the order item first.");
            return;
        }
        OrderItem item = (OrderItem) tblCart.getValueAt (selectedRowIndex, 0);
        int quant = 0;
        if (txtNewQuantity.getText()!=""){
            quant = Integer.parseInt (txtNewQuantity.getText());
        }
        OrderItem s=portfolio.findProduct(item.getProduct());
        if (s.getQuantity()<quant){
            JOptionPane.showMessageDialog(this, "Quantity more than availability.");
            return;
        }
        s.setQuantity(s.getQuantity()-quant+item.getQuantity());
        item.setQuantity(quant);
        populateTable();
        populateCartTable();
    }//GEN-LAST:event_btnModifyQuantityActionPerformed

    private void btnAddToCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddToCartActionPerformed
        // TODO add your handling code here:
        int selectedRowIndex = tblPortfolio.getSelectedRow();
        if (selectedRowIndex < 0) {
            JOptionPane.showMessageDialog(this, "Please select the product first.");
            return;
        }
        Stock stock =  stocks.searchStock(tblPortfolio.getValueAt (selectedRowIndex, 0).toString());
        OrderItem oi1 = (OrderItem) tblPortfolio.getValueAt (selectedRowIndex, 0);
        int quant = 0;
        quant = (Integer) spnQuantity.getValue();
        if (quant>oi1.getQuantity())
        {
            JOptionPane.showMessageDialog(this, "Quantity is more than availability.");
            return;
        }
        oi1.setQuantity(oi1.getQuantity()-quant);
        OrderItem item = currentOrder. findProduct (stock);
        if (item == null) {
            currentOrder.addNewOrderItem(stock, stock.getCurrentPrice(), quant);
        }
        else
            JOptionPane.showMessageDialog(this, "Stock already added.");
        populateTable();
        populateCartTable ();
    }//GEN-LAST:event_btnAddToCartActionPerformed

