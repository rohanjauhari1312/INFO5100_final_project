    private void populateTable(Order portfolio) {
                DefaultTableModel model = (DefaultTableModel) tblPortfolio.getModel();
        model. setRowCount (0) ;
        double sum=0;
        for (OrderItem oi : portfolio.getOrderItemList()) {
            Object row[] = new Object [4];
            row [0] = oi.getProduct().getName();
            row[1] = oi.getSalesPrice();
            row[2] = oi.getQuantity();
            row [3] = oi.getQuantity() * oi.getSalesPrice();
            sum=sum+oi.getQuantity() * oi.getSalesPrice();
            model.addRow (row);
        }
    }

