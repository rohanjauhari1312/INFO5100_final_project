    private void btnDeleteAccActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteAccActionPerformed
        // TODO add your handling code here:
        int response = JOptionPane.showConfirmDialog(
                null, 
                "Do you want to proceed?", // Message
                "Confirm",                 // Title
                JOptionPane.YES_NO_OPTION, // Option type
                JOptionPane.QUESTION_MESSAGE // Message type
        );

        // Handle the response
        if (response == JOptionPane.YES_OPTION) {
            admin.deRegister(p);
            LoginUsers lra = new LoginUsers(userProcessContainer,admin);
        userProcessContainer.add("LoginUsers",lra);
        CardLayout layout = (CardLayout)userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        } 
        
    }//GEN-LAST:event_btnDeleteAccActionPerformed

